import random
import math

def write_tsp_file(filename, name, comment, node_coords):
    with open(filename, 'w') as f:
        f.write(f"NAME : {name}\n")
        f.write(f"COMMENT : {comment}\n")
        f.write("TYPE : TSP\n")
        f.write(f"DIMENSION : {len(node_coords)}\n")
        f.write("EDGE_WEIGHT_TYPE : EUC_2D\n")
        f.write("NODE_COORD_SECTION\n")
        for node in node_coords:
            node_id, x, y = node
            f.write(f"{node_id} {x} {y}\n")
        f.write("EOF\n")

def generate_heart_shape_nodes(num_points=20, center_x=150, center_y=150, scale=10):
    coords = []
    for i in range(num_points):
        t = 2 * math.pi * i / num_points
        x = 16 * math.sin(t)**3
        y = 13 * math.cos(t) - 5 * math.cos(2*t) - 2 * math.cos(3*t) - math.cos(4*t)
        xi = int(center_x + scale * x)
        yi = int(center_y + scale * y)
        coords.append((i + 1, xi, yi))
    
    return coords

def main():
    tsp_name = "jam20"
    comment = "Generated by Jaemin Kim (20211061)"

    coords = generate_heart_shape_nodes()
    write_tsp_file("jam20.tsp", tsp_name, comment, coords)

if __name__ == "__main__":
    main()